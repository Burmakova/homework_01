# Для натурального числа К напечатать фразу «мы нашли К грибов в лесу»,
# согласовав окончание слова «гриб» с числом К.
from random import *

txt_end = ''
k = str(randrange(0, 1000))
print('Случайное число:', k)
if k[-1] in '056789':
    txt_end = 'ов'
elif k[-1] in '234':
    txt_end = 'а'
print(f'Мы нашли {k} гриб{txt_end} в лесу.')
