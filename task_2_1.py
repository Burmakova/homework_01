# Шестизначный автобусный билет считается удачным, если сумма его цифр делится на 7.
# Могут ли два билета подряд быть удачными?
from random import randrange

num_ticket = randrange(100000, 1000000)
print('Номер билета:', num_ticket)
if num_ticket % 7 == 0:
    print('### Билет удачный ###')
    print(f'Два подряд билета, удачными быть не могут т.к {num_ticket + 1} // 7 = {(num_ticket + 1) / 7}')

else:
    print('Билет обычный, увы.')
